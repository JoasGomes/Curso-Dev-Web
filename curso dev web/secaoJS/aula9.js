

        //funções para tarefas matemáticas
        //math.ceil = arredondamento para cima
        var x = Math.ceil(10.380)
        document.write(x)

        document.write('<br/>')

        //math.floor = arredondamento para baixo
        var x = Math.floor(10.380)
        document.write(x)

        document.write('<br/>')

        //math.round = arredondamento para cima ou para baixo 
        //(abaixo de 5 -> pra baixo acima de 5 -> pra cima)
        var x = Math.round(10.380)
        document.write(x)

        document.write('<br/>')

