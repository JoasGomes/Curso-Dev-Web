
        //propriedade length = retorna o numero de caracteres
        document.write('jorge'.length)


        document.write('<br/>')

        //propriedade charAt = indicamos um índice e ele retorna o caractere da posição
        document.write('jorge'.charAt(1))


        document.write('<br/>')

        //propriedade indexOf = indicamos um um caractere e ele mostra sua primeira ocorrência
        document.write('jorge'.indexOf('j'))


        document.write('<br/>')
        
        //propriedade replace = indicamos o que queremos mudar e para que irá mudar
        document.write('jorge'.replace('j','a'))


        document.write('<br/>')

        //propriedade substr = indicamos a posição inicial e quantas a partir dela mostrará
        document.write('jorge'.substr(2,2))

        
        document.write('<br/>')

        //propriedade toLowerCase = toda a string ficará em caixa baixa
        document.write('jorge'.toLocaleLowerCase())

        
        document.write('<br/>')

        //propriedade toUpperCase = toda a string ficará em caixa alta
        document.write('jorge'.toLocaleUpperCase())

        
        document.write('<br/>')


